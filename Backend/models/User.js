import mongoose from "mongoose";
import bcrypt from "bcrypt"

// users: With fields id, name, email, password.

// checkouts: With fields id, user_id, book_id, checkout_date, return_date.

const userSchema = mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
})
userSchema.pre("save", async function (next) {
  const user = this;
  if (user.isModified("password")) {
    user.password = await bcrypt.hash(user.password, 8);
  }
  next();
});

const User = mongoose.model("User", userSchema);
export default User;